#Aula_01
'''
SYMBOL  OPERATION
  +     addition
  -     subtraction
  *     multiplication
  /     division
'''

#Exemplo 01 
print(1 + 2 + 3 *3)
print((1 + 2 + 3) *3)

#Exemplo 02 - Exponenciação(exponention) Elevado a potência: **
print(3**2)

#Exemplo 03 - Logica Binaria (caret): ^ bitwise XOR
# Veja que ^ não faz esta operação, como você pode ter visto em outras linguagens

#Exemplo 04 - Resto da divisão - mod(remainder): %
print(9 % 2)

#Exemplo 05 - Divisão Inteiro(integer Division) Divide e arredonda para baixo até o inteiro mais próximo: //
print(7 // 3)
print(-7 // -3)








